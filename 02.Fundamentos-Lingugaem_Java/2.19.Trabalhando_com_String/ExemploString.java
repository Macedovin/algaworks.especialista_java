public class ExemploString {

	public static void main(String[] args) {
	
		System.out.println("Fala, mergulhador!");
	
		int x = 10;
		int y = 5;
		int z = x + y;
	
		System.out.println("Resultado: " + z);
		
		// Realizando a operação no método println
		System.out.println("Resultado: " + x + y);
		
		// Colocadas antes dos textos as variáveis
		System.out.println(x + y + " foi o resultado.");
	
		// Utilizando o GROUP OPERATOR/ OPERADOR DE AGRUPAMENTO (parênteses)
		System.out.println("Resultado: " + (x + y));
	}
}